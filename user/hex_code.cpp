void hex_encode(const unsigned char* from, size_t size, unsigned char* to) {
    for(size_t i = 0; i < size; ++i) {
        const unsigned char c = from[i];
        to[2 * i] = hex_table1[c];
        to[2 * i + 1] = hex_table2[c];
    }
}

void hex_decode(const unsigned char* from, size_t size, unsigned char* to) {
    const size_t size2 = size / 2;
    for(size_t i = 0; i < size2; ++i) {
        to[i] = hex_table3[from[2*i]] + hex_table4[from[2*i+1]];
    }
}

std::string hex_encode(const char* from, size_t size) {
    unsigned char* buffer = new unsigned char[2*size];
    hex_encode((const unsigned char*)from, size, buffer);
    string res = (char*)buffer;
    delete buffer;
    return res;
}

void hex_decode(std::string in, char* to) {
    hex_decode((unsigned char*)in.c_str(), in.size(), (unsigned char*)to);
}
